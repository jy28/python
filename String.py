# Single Quotes
x='spam eggs'
print(x)

# use \' to escape the single quote
x='doesn\'t'
print(x)

# use double quotes
x="doesn't"
print(x)

y='"Yes," he said.'
print(y)

y="\"Yes,\" he said."
print(y) 

y='"Isn\'t," he said.'
print(y)

print('"Isn\'t," she said.')

print("'Isn\'t,' she said.")

s = 'First line.\nSecond line.'
print(s)

print('C:\some\name')  #  \n means newline!

print(r'C:\some\name')  #  r before the quote

x="riti"
print("P"+x)

str = 'Priti'
print(str[:2])
print(str[2:])
print(str[5:])
print(str[::2])
